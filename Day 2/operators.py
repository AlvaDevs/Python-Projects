x = 7
y = 2

# Sum
print(f"{x} plus {y} is equal to {x + y}")

# Subtraction
print(f"{x} minus {y} is equal to {x - y}")

# Multiplication
print(f"{x} times {y} is equal to {x * y}")

# Division
print(f"{x} divides {y} is equal to {x / y}")

# Flor division
print(f"{x} divides {y} is equal to {x // y}")

# Modulus
print(f"{x} divides {y} is equal to {x % y}")

# Power
print(f"{x} to the power of {y} is equal to {x ** y}")

# Root
print(f"The squere root of {x} is equal to {x ** 0.5}")